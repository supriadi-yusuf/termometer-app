class TermoType:
  """ """""""""""""""""""""""""""""""""""""""""""""""""""""""""""""
  this class contains property to represent termometer type :
  1. Celcius
  2. Fahreinheit
  3. Riomer
  4. Kelvin  
  """"""""""""""""""""""""""""""""""""""""""""""""""""""""""""" """

  @property
  def celcius_termo(self):
    return '1'

  @property
  def fahreinheit_termo(self):
    return '2'

  @property
  def riomer_termo(self):
    return '3'

  @property
  def kelvin_termo(self):
    return '4'
